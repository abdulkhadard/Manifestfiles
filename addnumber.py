# Function to add two numbers
def add_numbers(a, b):
    return a + b

# Input from the user
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

# Calculate the sum
sum_result = add_numbers(num1, num2)

# Output the result
print(f"The sum of {num1} and {num2} is: {sum_result}")

